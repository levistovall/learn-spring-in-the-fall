package com.levi;

public class Student 
{
	private Integer age;
	private Integer id;
	private String name;

	public Integer getAge()
	{
		return age;
	}
	
	public void setAge(Integer anAge)
	{
		age = anAge;
	}
	
	public Integer getId()
	{
		return id;
	}
	
	public void setId(Integer anId)
	{
		id = anId;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String aName)
	{
		name = aName;
	}
	
}
